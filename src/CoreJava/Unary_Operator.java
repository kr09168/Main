package CoreJava;

public class Unary_Operator {

	public static void main(String[] args) {
		int a = 1;
		
		System.out.println(a++);//post increment //1
		System.out.println(++a);//pre increment //3
		System.out.println(a--);//post decrement //3
		System.out.println(--a);//pre decrement	//1
		
	}

}
