package CoreJava;

public class Pre_Data_Types {

	public static void main(String[] args) {
		
		byte a = 10; // 1 byte = 8 bits. Default value :- 0.
		System.out.println(a);
		short b = 20; // 2 bytes = 16 bits Default value :- 0. 
		System.out.println(b);
		int c = 30; // 4 bytes = 32 bits Default value :- 0.
		System.out.println(c);
		long d = 40;// 8 bytes = 64 bits Default value :- 0.
		System.out.println(d);
		float e = 50;//4bytes = 32 bits. Default value :- 0.0.
		System.out.println(e);
		double f = 60;//8bytes = 64 bits Default value :- 0.0.
		System.out.println(f);
		char g = 'k';// 2bytes = 16bits Default value :- null.
		System.out.println(g);
		boolean h = (a>b); // less than one bytes. Default value :- false.
		System.out.println(h);
		
		
		
	}

}
